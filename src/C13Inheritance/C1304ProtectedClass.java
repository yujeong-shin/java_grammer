package C13Inheritance;

//public : 프로젝트 전체에서 접근 가능
//protected : 패키지를 벗어나더라도 상속 관계인 경우엔 접근 가능
//default : 같은 패키지 내에서만 접근 가능
//private : 같은 클래스 내에서만 접근 가능
public class C1304ProtectedClass {
    private String str1 = "hello java1";
    String str2 = "hello java2";
    protected String str3 = "hello java3";
    public String str4 = "hello java4";

}
